const strUrl =
  "^((https|http)://)" +
  "?(([0-9a-z_!~*'().&=+$%-]+: )?[0-9a-z_!~*'().&=+$%-]+@)?" +
  "(([0-9]{1,3}.){3}[0-9]{1,3}" +
  "|" +
  "([0-9a-z_!~*'()-]+.)*" +
  "([0-9a-z][0-9a-z-]{0,61})?[0-9a-z]." +
  "[a-z]{2,6})" +
  "(:[0-9]{1,4})?" +
  "((/?)|" +
  "(/[0-9a-z_!~*'().;?:@&=+$,%#-]+)+/?)$";
export default function checkUrl(url) {
  return new RegExp(strUrl).test(url);
}
